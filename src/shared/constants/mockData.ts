export const getSectionGames = (
  sectionId: string,
  allGames: Game[],
  favoriteIds: string[]
): Game[] => {
  switch (sectionId) {
    case 'most-popular':
      return [...allGames]
        .sort((a, b) => b.popularity - a.popularity)
        .slice(0, 6)
        .map((game) => ({ ...game, isFavorite: favoriteIds.includes(game.id) }));

    case 'most-played-region':
      return [...allGames]
        .filter((game) => game.region === 'Dubai')
        .sort((a, b) => b.playerCount - a.playerCount)
        .slice(0, 10)
        .map((game) => ({ ...game, isFavorite: favoriteIds.includes(game.id) }));

    case 'favorites':
      return allGames
        .filter((game) => favoriteIds.includes(game.id))
        .map((game) => ({ ...game, isFavorite: true }));

    default:
      return [];
  }
};